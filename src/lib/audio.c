// sample_resmaple uses the bandlimited intrepolation algorithm described in
// docs/resample.pdf
// This algorithme trims the first and last 13 samples from the original sample
// out of the resampled sample. To avoid loosing these 13 samples you can set
// `trim` to false. This will add 13 zeros and the start and the end of the
// input sample. That way sample_resample will trim the 13 zeros instead of
// trimming samples from the original sample.
// 
// Time Register:
// n_n = 24, n_l = 6, n_mu = 2
// than mean that input->sample_count is bound to 2^n_n - 1
//
// Low Pass Filter Table (h):
// N_z = 13
// than mean len(filter_table) = N_z * 2^n_l + 1 = 833
// also we drop the 13 first and last samples from input
Sample sample_resample(Arena *arena, Sample input, u32 output_rate, b32 trim) {
  u32 Nz = 13;
  u32 L = 64;

  f32 rho = (f32) output_rate / (f32) input.sample_rate;
  u32 output_sample_count = trim ? (f32) (input.sample_count - 2*Nz) * rho : (f32) input.sample_count * rho;
  Sample output = sample_alloc(arena, output_rate, output_sample_count, input.channel_count);

  if (input.channel_count == 1) {
    if (trim) {
      for (usize i = 0; i < output_sample_count; ++i) {
        u32 time_register = ((f32) Nz - 1.0f + (f32) i / rho) * 256.0f;
        u32 n = time_register >> 8;
        u32 l = (time_register >> 2) & 0x3f;
        f32 eta = (f32) (time_register & 0x3) / 4;

        output.buf[i] = 0;
        for (usize j = 0; j < 13; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n - j] * h;
        }
        l = 63 - l;
        eta = 1 - eta;
        for (usize j = 0; j < 13; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n + 1 + j] * h;
        }
      }
    } else {
      for (usize i = 0; i < output_sample_count; ++i) {
        u32 time_register = (f32) i / rho * 256.0f;
        u32 n = time_register >> 8;
        u32 l = (time_register >> 2) & 0x3f;
        f32 eta = (f32) (time_register & 0x3) / 4;

        output.buf[i] = 0;
        for (usize j = 0; j < 13 && n - j >= 0; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n - j] * h;
        }
        l = 63 - l;
        eta = 1 - eta;
        for (usize j = 0; j < 13 && n + 1 + j < input.sample_count; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n + 1 + j] * h;
        }
      }
    }
  } else {
    if (trim) {
      for (usize i = 0; i < output_sample_count; ++i) {
        u32 time_register = ((f32) Nz - 1.0f + (f32) i / rho) * 256.0f;
        u32 n = time_register >> 8;
        u32 l = (time_register >> 2) & 0x3f;
        f32 eta = (f32) (time_register & 0x3) / 4;

        output.buf[i] = 0;
        for (usize j = 0; j < 13; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n - j] * h;
          output.buf[output.sample_count + i] += input.buf[input.sample_count + n - j] * h;
        }
        l = 63 - l;
        eta = 1 - eta;
        for (usize j = 0; j < 13; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n + 1 + j] * h;
          output.buf[output.sample_count + i] += input.buf[input.sample_count + n + 1 + j] * h;
        }
      }
    } else {
      for (usize i = 0; i < output_sample_count; ++i) {
        u32 time_register = (f32) i / rho * 256.0f;
        u32 n = time_register >> 8;
        u32 l = (time_register >> 2) & 0x3f;
        f32 eta = (f32) (time_register & 0x3) / 4;

        output.buf[i] = 0;
        for (usize j = 0; j < 13 && n - j >= 0; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n - j] * h;
          output.buf[output.sample_count + i] += input.buf[input.sample_count + n - j] * h;
        }
        l = 63 - l;
        eta = 1 - eta;
        for (usize j = 0; j < 13; ++j) {
          f32 h = (1 - eta) * sample_filter_table[l + j*L] + eta * sample_filter_table[l + j*L + 1];
          output.buf[i] += input.buf[n + 1 + j] * h;
          output.buf[output.sample_count + i] += input.buf[input.sample_count + n + 1 + j] * h;
        }
      }
    }
  }

  return output;
}

void audio_context_play_audio(AudioContext *context, Audio audio, f32 gain) {
  assert(context->audio_count + 1 <= AC_AUDIO_CAP);
  assert(audio.sample.sample_rate == AL_RATE);
  context->audio[context->audio_count] = audio;
  context->audio_gain[context->audio_count] = gain;
  context->audio_count += 1;
}

void audio_context_remove_audio(AudioContext *context, usize index) {
  assert(index < context->audio_count);
  context->audio[index] = context->audio[context->audio_count-1];
  context->audio_gain[index] = context->audio_gain[context->audio_count-1];
  context->audio_count -= 1;
}

void audio_context_push_samples(Arena *arena, AudioContext *context) {
  ArenaTemp scratch = arena_scratch_get(arena);

  u32 channel_count = 1;
  for (usize i = 0; i < context->audio_count; ++i) {
    if (context->audio[i].sample.channel_count > channel_count) {
      channel_count = context->audio[i].sample.channel_count;
    }
  }
  assert(channel_count == 1);
  // assert(channel_count > 0 && channel_count < 2);

  f32 *buf = arena_push(scratch.arena, sizeof(f32) * channel_count * AL_BATCH_SIZE);
  mem_zero(buf, sizeof(f32) * channel_count * AL_BATCH_SIZE);

  // mixing
  for (usize i = 0; i < context->audio_count; ++i) {
    Sample sample = context->audio[i].sample;
    f32 gain = context->audio_gain[i];

    assert(channel_count == 1);
    for (usize j = 0; j < AL_BATCH_SIZE; ++j) {
      if (context->audio[i].index >= sample.sample_count) {
        audio_context_remove_audio(context, i);
        i -= 1;
        break;
      }
      buf[j] += sample.buf[context->audio[i].index++] * gain;
    }
  }

  // hard limiter
  if (channel_count == 1) {
    for (usize i = 0; i < AL_BATCH_SIZE; ++i) {
      if (f32_abs(buf[i]) > 1) {
        buf[i] = buf[i] < 0 ? -1 : 1;
      }
    }
  } else {
    for (usize i = 0; i < 2*AL_BATCH_SIZE; ++i) {
      if (f32_abs(buf[i]) > 1) {
        buf[i] = buf[i] < 0 ? -1 : 1;
      }
    }
  }

  if (channel_count == 1) {
    al_push_samples(buf, buf);
  } else {
    al_push_samples(buf, buf + AL_BATCH_SIZE);
  }
  arena_scratch_release(scratch);
}
